package com.knowhow.mypage.domain;

public class MyQuestionDTO {

	private Long questionId;
	private String questionTitle;
	private String questionContent;
	private String questionRegdate;
	private String questionUpddate;
	private Long memberId;
	private Long categoryId;
	private String categoryName;
	private Long countAnswer;
	
	public MyQuestionDTO() {;}

	public Long getQuestionId() {
		return questionId;
	}

	public void setQuestionId(Long questionId) {
		this.questionId = questionId;
	}

	public String getQuestionTitle() {
		return questionTitle;
	}

	public void setQuestionTitle(String questionTitle) {
		this.questionTitle = questionTitle;
	}

	public String getQuestionContent() {
		return questionContent;
	}

	public void setQuestionContent(String questionContent) {
		this.questionContent = questionContent;
	}

	public String getQuestionRegdate() {
		return questionRegdate;
	}

	public void setQuestionRegdate(String questionRegdate) {
		this.questionRegdate = questionRegdate;
	}

	public String getQuestionUpddate() {
		return questionUpddate;
	}

	public void setQuestionUpddate(String questionUpddate) {
		this.questionUpddate = questionUpddate;
	}

	public Long getMemberId() {
		return memberId;
	}

	public void setMemberId(Long memberId) {
		this.memberId = memberId;
	}

	public Long getCategoryId() {
		return categoryId;
	}

	public void setCategoryId(Long categoryId) {
		this.categoryId = categoryId;
	}

	public String getCategoryName() {
		return categoryName;
	}

	public void setCategoryName(String categoryName) {
		this.categoryName = categoryName;
	}

	public Long getCountAnswer() {
		return countAnswer;
	}

	public void setCountAnswer(Long countAnswer) {
		this.countAnswer = countAnswer;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((categoryId == null) ? 0 : categoryId.hashCode());
		result = prime * result + ((memberId == null) ? 0 : memberId.hashCode());
		result = prime * result + ((questionId == null) ? 0 : questionId.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		MyQuestionDTO other = (MyQuestionDTO) obj;
		if (categoryId == null) {
			if (other.categoryId != null)
				return false;
		} else if (!categoryId.equals(other.categoryId))
			return false;
		if (memberId == null) {
			if (other.memberId != null)
				return false;
		} else if (!memberId.equals(other.memberId))
			return false;
		if (questionId == null) {
			if (other.questionId != null)
				return false;
		} else if (!questionId.equals(other.questionId))
			return false;
		return true;
	}

	
}
